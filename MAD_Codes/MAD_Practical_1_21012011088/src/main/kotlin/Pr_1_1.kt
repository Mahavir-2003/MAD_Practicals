//que : Store & Display values in different variable of different type (Integer, Double, Float, Long, Short, Byte, Char, Boolean, String)

fun main(args: Array<String>) {
    val a: Int = 5
    val b: Double = 3.1423453
    val c: Float = 8.133235F
    val d: Long = 1324342
    val e: Short = 124
    val f: Byte = 0
    val g: Boolean = true
    val h: String = "Hello World!"

    println("Integer Value: $a")
    println("Double Value: $b")
    println("Float Value: $c")
    println("Long Value: $d")
    println("Short Value: $e")
    println("Byte Value: $f")
    println("Boolean Value: $g")
    println("String Value: $h")
}